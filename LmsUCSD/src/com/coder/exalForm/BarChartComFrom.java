package com.coder.exalForm;

import java.util.List;

public class BarChartComFrom {
private String fileName;
private String title;
private String vertical;
private String horizontal;
private String total;
private List<BarChartForm> barChartForms;
public String getFileName() {
	return fileName;
}
public void setFileName(String fileName) {
	this.fileName = fileName;
}
public String getTitle() {
	return title;
}
public void setTitle(String title) {
	this.title = title;
}
public String getVertical() {
	return vertical;
}
public void setVertical(String vertical) {
	this.vertical = vertical;
}
public String getHorizontal() {
	return horizontal;
}
public void setHorizontal(String horizontal) {
	this.horizontal = horizontal;
}
public List<BarChartForm> getBarChartForms() {
	return barChartForms;
}
public void setBarChartForms(List<BarChartForm> barChartForms) {
	this.barChartForms = barChartForms;
}
public String getTotal() {
	return total;
}
public void setTotal(String total) {
	this.total = total;
}

}
 